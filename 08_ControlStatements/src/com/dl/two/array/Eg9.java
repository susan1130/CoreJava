package com.dl.two.array;

public class Eg9 {

	public static void main(String[] args) {
		//a.length() returns an integer on which arithmetic operations are possible
		
		int a[] = {20,30,40};
		System.out.println(a.length);
		System.out.println(a.length/2);
		System.out.println(a.length*2);
		System.out.println(a.length%2);
		System.out.println(a.length+2);
		System.out.println(a.length);

	}

}
