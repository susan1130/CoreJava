package com.dl.one;

public class Eg3 {

	public static void main(String[] args) {
		// for loop
		for(int i = 0; i<9 ; i++) {
			System.out.println(i);
		}
		
		// while loop
		int a = 10;
		while (a>0) {
			System.out.println(a);
			a--;
		}
		
		// do while loop
		int j = 5;
		do {
			System.out.println(j);
			j--;
		}while (j>0);

	}

}
