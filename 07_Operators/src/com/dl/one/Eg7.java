package com.dl.one;

public class Eg7 {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 5;
		
		System.out.println(a & b);
		System.out.println(a | b);
		System.out.println(a ^ b);
		
		int c = 10;
		int d = 2;
		
		System.out.println(c & d);
		System.out.println(c | d);
		System.out.println(c ^ d);
	}

}
//0b0010 -> 2
//0b1010 -> 10
//0b1000 -> 8



