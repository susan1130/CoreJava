package com.dl.one;

public class Eg1 {
// primitive data type default value
	static byte b;
	static short s;
	static int i;
	static long l;
	
	static float f;
	static double d;
	
	static boolean bo;
	
	static char ch;
	
	public static void main(String[] args) {
		// default value of the primitive data types
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(bo);
		System.out.println(ch);
		System.out.println("The default for char is a blank space ASCII 32");

	}

}
