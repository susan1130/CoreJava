package com.dl.one;

public class Eg4 {

	public static void main(String[] args) {

		int a = 10;			//0-9 		base 10
		int b = 0125;		//07 		base 8
		int c = 0Xaf09;		// 0-9 a-f 	base 16
		int d = 0b10110;	// 0 and 1  base 2
		
		System.out.println("Decimal number system: "+a);
		System.out.println("Octal number system: "+b);
		System.out.println("Hexadecimal number system: "+c);
		System.out.println("Binary number system: "+d);
	}

}
